import random

number = random.randint(1, 7)

if number == 1:
    print("violet")
elif number == 2:
    print("indigo")
elif number == 3:
    print("blue")
elif number == 4:
    print("green")
elif number == 5:
    print("yellow")
elif number == 6:
    print("orange")
elif number == 7:
    print("red")
